const searchBox = document.querySelector("#searchBox");
const nextBtn = document.querySelector("#nextBtn");
const prevBtn = document.querySelector("#prevBtn");
const clearBtn = document.querySelector("#clearBtn");
prevBtn.addEventListener("click", handlePrevBtn);
nextBtn.addEventListener("click", handleNextBtn);
searchBox.addEventListener("keypress", searchResume);
clearBtn.addEventListener("click", clearSearch);

const personName = document.querySelector("#name");
const position = document.querySelector("#position");
const photo = document.querySelector(".photo");
const phone = document.querySelector(".phone");
const email = document.querySelector(".email");
const linkedIn = document.querySelector(".linkedIn");
const skills = document.querySelector("#skills");
const hobbies = document.querySelector("#hobbies");
const companyInfo = document.querySelector(".companyInfo");
const summary = document.querySelector(".summary");
const projectDesc = document.querySelector(".projectDesc");
const educationData = document.querySelector("#educationData");
const achievementData = document.querySelector("#achievementData");
const internship = document.querySelector(".internshipCompanyInfo")
const internshipSummary = document.querySelector(".internshipSummary")


let resumIndex = 0;
loadResumeData(resumIndex);
function loadResumeData(index) {
    const resumeItem = jsonData.resume[index];
    personName.innerText = resumeItem.basics.name;
    position.innerText = `Applied For : ${resumeItem.basics.AppliedFor}`;
    photo.src = !!resumeItem.basics.image ?resumeItem.basics.image :"assets/user.png";
    phone.innerText = resumeItem.basics.phone;
    email.getElementsByTagName('a')[0].href = resumeItem.basics.email;
    email.getElementsByTagName('a')[0].innerText = resumeItem.basics.email;
    linkedIn.getElementsByTagName("a")[0].href = resumeItem.basics.profiles.url;
    summary.innerText = resumeItem.work.Summary;
    internshipSummary.innerText = resumeItem.Internship.Summary;
    projectDesc.innerHTML = `<span class="pName">${resumeItem.projects.name}</span> ${resumeItem. projects.description}`
    achievementData.innerHTML = `<li>${resumeItem.achievements.Summary}</li>`;
    showSkills(resumeItem.skills.keywords);
    showEducation(resumeItem.education);
    showHobbies(resumeItem.interests.hobbies);
    showInternship(resumeItem.Internship);
    showWork(resumeItem.work);
}

function handleNextBtn(e) {
    if(resumIndex < jsonData.resume.length - 1) {
        resumIndex++;
        loadResumeData(resumIndex);
    }
	console.log(resumIndex, jsonData.resume.length - 1);
    if(resumIndex == jsonData.resume.length - 1) {
        nextBtn.style.visibility = 'hidden';
    } 
	if(resumIndex >= 0) {
        prevBtn.style.visibility = 'visible';
    }
}

function handlePrevBtn(e) {
    if(resumIndex != 0) {
        resumIndex--;
        loadResumeData(resumIndex);
    }
    if(resumIndex  ==  jsonData.resume.length-1) {
        nextBtn.style.visibility = 'hidden';
    } 
	if (resumIndex <  jsonData.resume.length-1) {
        nextBtn.style.visibility = 'visible';
        if(resumIndex==0) {
            prevBtn.style.visibility = 'hidden';
        }
    } 
}

function clearSearch(e) {
	searchBox.value="";
	jsonData = data;
	resumIndex = 0;
	nextBtn.style.visibility = 'visible';
	prevBtn.style.visibility = 'hidden';
	document.querySelector("#noResultFound").style.display = "none";
    document.querySelector("#resultFound").style.display = "block";
	loadResumeData(resumIndex);
}

function searchResume(e) {
    if(e.key === "Enter") {
		let searchedData = jsonData.resume.filter((item) => item.skills.keywords.includes(searchBox.value) || item.basics.AppliedFor.toLowerCase() === searchBox.value.toLowerCase() );
		jsonData = {
            resume : searchedData
        }
        if(searchedData.length === 0) {
            document.querySelector("#noResultFound").style.display = "block";
            document.querySelector("#resultFound").style.display = "none";
            nextBtn.style.visibility = 'hidden';
            prevBtn.style.visibility = 'hidden';
        } else if(searchedData.length === 1) {
			nextBtn.style.visibility = 'hidden';
            prevBtn.style.visibility = 'hidden';
			resumIndex = 0;
            loadResumeData(resumIndex);
		} else {
            resumIndex = 0;
            loadResumeData(resumIndex);
            nextBtn.style.visibility = 'visible';
            prevBtn.style.visibility = 'hidden';
        }
    }
}

function showWork(workItem){
    while (companyInfo.firstChild) {
        companyInfo.removeChild(companyInfo.firstChild);
    }
    for (const key in workItem) {
        if(key !== "Summary" ) {
            const paragraph = document.createElement('p');
            paragraph.textContent = `${key} : ${workItem[key]}`;
            companyInfo.appendChild(paragraph);
        }
    }
}

